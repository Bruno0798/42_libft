#include "libft.h"

void	*ft_bzero(void *b, size_t len)
{
	size_t	i;
	char	*c;

	i = 0;
	c = (char *)b;
	while (i < len)
	{
		c[i] = 0;
		i++;
	}
	return (NULL);
}
